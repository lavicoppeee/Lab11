package it.polito.tdp.rivers.model;

public class Result {

	double avgC;
	int numDays;

	public Result(double avgC, int numDays) {
		this.avgC = avgC;
		this.numDays = numDays;
	}

	public double getAvgC() {
		return avgC;
	}

	public void setAvgC(double avgC) {
		this.avgC = avgC;
	}

	public int getNumDays() {
		return numDays;
	}

	public void setNumDays(int numDays) {
		this.numDays = numDays;
	}

	
	
	
}
